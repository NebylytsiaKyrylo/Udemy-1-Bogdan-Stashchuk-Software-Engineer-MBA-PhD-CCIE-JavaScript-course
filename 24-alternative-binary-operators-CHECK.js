/** TASK 24 - Alternative Binary Operators
 *
 * In each expression, replace two operators with one binary
 */

let a = 10;

// a = a + 1;
a += 1;
console.log(a); // 11

// a = a * 2;
a *= 2;
console.log(a); // 22

// a = a - 5;
a -= 5;
console.log(a); // 17

// a = a / 2;
a /= 2;
console.log(a); // 8.5
